# Фиксированные взносы 2025
FIXED_INSURANCE_2025 = 53_658


def extract_numbers(text: str) -> list:
    """Извлечь все числа из текста."""
    # Убираем слова-помехи
    cleaned = text.replace("₽", "").replace("руб", "").replace("р.", "").replace("р ", " ")
    # Находим все числа (целые и с точкой/запятой)
    numbers = re.findall(r'\d[\d\s]*[\d.,]?\d*', cleaned)
    result = []
    for n in numbers:
        n = n.replace(" ", "").replace(",", ".")
        try:
            val = float(n)
            if val >= 100:  # игнорируем мелкие числа (номера кварталов и т.п.)
                result.append(val)
        except ValueError:
            continue
    return result


def calculate_quick_tax(amounts: list) -> str:
    """Быстрый расчёт налога по списку сумм."""
    total = sum(amounts)
    tax_gross = round(total * 0.06, 2)
    insurance = FIXED_INSURANCE_2025
    extra_1pct = calculate_1pct_insurance(total)
    total_insurance = insurance + extra_1pct
    tax_after_deduction = max(0, tax_gross - total_insurance)

    lines = [
        "Расчёт налога УСН 6% за 2025 год:\n",
    ]

    if len(amounts) > 1:
        for i, amt in enumerate(amounts, 1):
            lines.append(f"  Сумма {i}: {format_money(amt)}")
        lines.append("")

    lines.append(f"  Общий доход: {format_money(total)}")
    lines.append(f"  Налог 6%: {format_money(tax_gross)}")
    lines.append(f"\n  Вычет страховых взносов:")
    lines.append(f"  Фиксированные взносы: {format_money(insurance)}")

    if extra_1pct > 0:
        lines.append(f"  1% с превышения 300 000: {format_money(extra_1pct)}")

    lines.append(f"  Итого взносов: {format_money(total_insurance)}")
    lines.append(f"\n  Налог после вычета взносов: {format_money(tax_after_deduction)}")

    if tax_after_deduction == 0:
        lines.append(f"\n  Налог полностью покрыт взносами!")
        lines.append(f"  Вам нужно только уплатить взносы: {format_money(total_insurance)}")
    else:
        total_to_pay = tax_after_deduction + total_insurance
        lines.append(f"\n  ИТОГО к уплате:")
        lines.append(f"  Взносы: {format_money(total_insurance)}")
        lines.append(f"  Налог УСН: {format_money(tax_after_deduction)}")
        lines.append(f"  ВСЕГО: {format_money(total_to_pay)}")

    lines.append(f"\n  Для точного расчёта по кварталам используйте /income и /calculate")
    lines.append(f"  Для расчёта взносов при закрытии ИП: /closeip ГГГГ-ММ-ДД")

    return "\n".join(lines)


def find_answer(text: str) -> str:
    """Найти ответ: сначала проверяем числа, потом ключевые слова."""
    # Сначала проверяем — есть ли в тексте суммы для расчёта
    numbers = extract_numbers(text)
    if numbers:
        return calculate_quick_tax(numbers)

    # Потом ищем по ключевым словам
    text_lower = text.lower()
    for keyword, answer in ANSWERS.items():
        if keyword in text_lower:
            return answer

    return DEFAULT_ANSWER
